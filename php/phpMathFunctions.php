<?php

    /**
     *  Math function predefined in php library;
     */

     // Floor
     echo("Floor of -7.8: " . floor(-7.8) . "<br>");
     echo("Floor of 7.8: " . floor(7.8) . "<br>");
     echo("<hr>");

     // Ceil
     echo("Ceil of -7.8: " . ceil(-7.8) . "<br>");
     echo("Ceil of 7.8: " . ceil(7.8) . "<br>");
     echo("<hr>");

     // Absolute
     echo("Abs of 7.8: " . abs(7.8) . "<br>");
     echo("Abs of 7.0: " . abs(7.0) . "<br>");
     echo("Abs of -7.8: " . abs(-7.8) . "<br>");
     echo("<hr>");

     // Square Root
     echo("Sqrt of 7.8: " . sqrt(7.8) . "<br>");
     echo("Sqrt of 8: " . sqrt(8) . "<br>");
     echo("Sqrt of 9: " . sqrt(9) . "<br>");
     echo("<hr>");

     // decimal to binary
     echo("Decimal to binary of 989957589: " . decbin(989957589) . "<br>");
     echo("Decimal to binary of -989957589: " . decbin(-989957589) . "<br>");
     echo("<hr>");

     // binary to decimal
     echo("Binary to Decimal of 1100101001: ". bindec(1100101001)."<br>");
     echo("Binary to Decimal of -1100101001: ". bindec(-1100101001)."<br>");
     echo("Binary to Decimal of 1110101001: ". bindec(1110101001)."<br>");
     echo("Binary to Decimal of -1110101001 ". bindec(-1110101001)."<br>");
     echo("<hr>");

     // decimal to hexadecimal
     echo("Decimal to Hexadecimal of 9899575829: " . dechex(9899575829) . "<br>");
     echo("Decimal to Hexadecimal of -9899575829: " . dechex(-9899575829) . "<br>");
     echo("<hr>");

     // Hexadecimal to Decimal
     echo("Hexadecimal to Decimal of 9899575829: " . hexdec(9899575829) . "<br>");
     echo("Hexadecimal to Decimal of -9899575829: " . hexdec(-9899575829) . "<br>");
     echo("<hr>");

     // decimal to octal
     echo("Decimal to Octal of 9899575829: " . decoct(9899575829) ."<br>");
     echo("Decimal to Octal of -9899575829: " . decoct(-9899575829) . "<br>");
     echo("<hr>");

     // octal to decimal
     echo("Octal to Decimal of 9899575829: " . octdec(9899575829) . "<br>");
     echo("Octal to Decimal of -9899575829: " . octdec(-9899575829) . "<br>");

?>